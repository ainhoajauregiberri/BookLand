package productos.libros;

/**
 * Clase que representa a las editoriales de los libros que están en la biblioteca.
 * @author ainhoa y lorea
 *
 */

public class Editorial {

	private String nomEditorial;

	public Editorial(String nomEditorial) {
		this.nomEditorial = nomEditorial;
	}

	public String getNomEditorial() {
		return nomEditorial;
	}

	public void setNomEditorial(String nomEditorial) {
		this.nomEditorial = nomEditorial;
	}
	
	
	
	
}
